// Arrays
var user1 = ["Rahul", 25, "male", "Bangalore", "coding"];
console.log(user1[3]);

// Objects
var user2 = {
  name: "Rahul",
  age: 25,
  gender: "male",
  city: "Bangalore",
  hobbies: "coding",
  marks: [25, 100, 80, 90, 80]
};
// Bracket Notation
console.log(user2["name"]);
console.log(user2["marks"][2]);
// Dot Notation
console.log(user2.gender);
console.log(user2.marks.length);
